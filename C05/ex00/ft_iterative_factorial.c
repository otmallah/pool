int	ft_iterative_factorial(int nb)
{
	int	r;
	int	a;

	r = 1;
	a = 1;
	if (nb == 0 || nb == 1)
		return (1);
	if (nb < 0)
		return (0);
	while (a <= nb)
	{
		r = r * a;
		a++;
	}
	return (r);
}
