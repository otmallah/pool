#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	space(void)
{
	write (1, ",", 1);
	write (1, " ", 1);
}

void	ft_print_comb(void)
{
	char	a;
	char	b;
	char	c;

	a = '0';
	while (a <= '7')
	{
		b = a + 1;
		while (b <= '8')
		{
			c = b + 1;
			while (c <= '9')
			{
				write (1, &a, 1);
				write (1, &b, 1);
				write (1, &c, 1);
				if (a != '7' )
					space();
				c++;
			}
			b++;
		}
		a++;
	}
}
