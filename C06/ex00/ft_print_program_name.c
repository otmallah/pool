#include <unistd.h>

int	main(int c, char **v)
{
	int	i;

	i = 0;
	if (c >= 1)
	{
		while (v[0][i])
		{
			write (1, &v[0][i], 1);
			i++;
		}
		write(1, "\n", 1);
	}
	return (0);
}
