#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	a;

	i = 0;
	a = 1;
	while (a <= (ac - 1))
	{
		while (av[a][i])
		{
			write (1, &av[a][i], 1);
			i++;
		}
		write (1, "\n", 1);
		i = 0;
		a++;
	}
	return (0);
}
