int	ft_atoi(char *str)
{
	int	i;
	int	res;
	int	s;

	i = 0;
	res = 0;
	s = 1;
	while ((str[i] == '\t') || (str[i] == '\r')
		|| (str[i] == ' ') || (str[i] == '\v')
		|| (str[i] == '\f') || (str[i] == '\n'))
		i++;
	while ((str[i] == '-') || (str[i] == '+'))
	{
		if (str[i] == '-')
			s = -s;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = ((str[i] - '0') + (res * 10));
		if ((str[i + 1] < '0') || (str[i + 1] > '9'))
			return (res * s);
		i++;
	}
	return (0);
}
