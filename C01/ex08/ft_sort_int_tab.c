void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	H;

	i = 0;
	j = size - 1;
	while (i < j)
	{
		if (tab[i + 1] < tab[i])
		{
			H = tab[i + 1];
			tab [i + 1] = tab[i];
			tab[i] = H;
			i = -1;
		}
		i++;
	}
}
